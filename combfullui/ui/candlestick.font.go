var characters = "0123456789-.:+"

type Digit struct {
	Character byte
	X int64
	Y int64
}

func (d *Digit) Draw(c PriceCandleCanvas) {
	var minx, maxx = d.X - 2, d.X + 1
	var miny, maxy = d.Y - 3, d.Y + 3
	var i = 0
	for y := miny; y <= maxy; y++ {
		var j = 0
		for x := minx; x <= maxx; x++ {
			if font[i][int(d.Character)*5 + j] == '#' {
				c.Img.Set(int(x),int(y), DefaultPriceGridOptions.Color)
			}
			j++
		}
		i++
	}
}
